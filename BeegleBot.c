void simpleAuton()
{
	motor[intake] = 127;
	goRobit(127, 127, TILE * 2.25);
	motor[intake] = 0;

}

void autonRunner()
{
	auton = 0;
	// !SensorValue defines if the pin is in (not out)
	if(!SensorValue(aut0))
	{
		auton += 1;
	}
	if(!SensorValue(aut1))
	{
		auton += 2;
	}
	if(!SensorValue(aut2))
	{
		auton += 4;
	}

	switch(auton)
	{
		case 0:
			simpleAuton();
			break;
		case 1:
			BFAuton();
			break;
		case 2:
			BPAuton();
			break;
		case 3:
			RFAuton();
			break;
		case 4:
			RPAuton();
			break;
		default:
			simpleAuton();
			break;
	}
}

void pre_auton() { }

task autonomous
{
	autonRunner();
}

task usercontrol
{
	while(true)
	{
		if(vexRT[Btn7U])
		{
			autonRunner();
		}

		if(SensorValue(driveModePin))
		{
			driveMode = 0;
		}
		else
		{
			driveMode = 1;
		}

		if(vexRT[Btn8U] || vexRT[Btn8D] || vexRT[Btn8L] || vexRT[Btn8R])
		{
			fineControl();
		}
		else
		{
			if(driveMode)
			{
				arcade(vexRT[Ch1], vexRT[Ch2]);
			}
			else
			{
				tank(vexRT[Ch3], vexRT[Ch2]);
			}
		}
		if(vexRT[Btn5U])
		{
			motor[arm] = 127;
		}
		else if(vexRT[Btn5D])
		{
			motor[arm] = -127;
		}
		else
		{
			motor[arm] = 0;
		}
		if(vexRT[Btn6U])
		{
			motor[intake] = 127;
		}
		else if(vexRT[Btn6D])
		{
			motor[intake] = -127;
		}
		else
		{
			motor[intake] = 0;
		}
	}
}
void fineControl()
{
	if(vexRT[Btn8U])
	{
		LDrive(40);
		RDrive(-40);
	}
	else if(vexRT[Btn8R])
	{
		LDrive(40);
		RDrive(40);
	}
	else if(vexRT[Btn8L])
	{
		LDrive(-40);
		RDrive(-40);
	}
	else if(vexRT[Btn8D])
	{
		LDrive(-40);
		RDrive(40);
	}
	else
	{
		LDrive(0);
		RDrive(0);
	}
}
