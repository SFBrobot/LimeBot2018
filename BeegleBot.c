int auton = 0;

//Tank is 0, arcade is 1
short driveMode;

#include "Vex_Competition_Includes.c"

void LDrive(int pwr)
{
	motor[L1] = motor[L2] = motor[L3] = motor[L4] = pwr;
}
void RDrive(int pwr)
{
	motor[R1] = motor[R2] = motor[R3] = motor[R4] = pwr;
}

void arcade(int mov, int rot)
{
	LDrive(mov + rot);
	RDrive(mov - rot);
}

void tank(int ld, int rd)
{
	LDrive(ld);
	RDrive(-    rd);
}

void goRobit(int lpwr, int rpwr, int time)
{
	LDrive(lpwr);
	RDrive(-rpwr);
	wait1Msec(time);
	LDrive(0);
	RDrive(0);
}

void goRobit(int lpwr, int rpwr)
{
	LDrive(lpwr);
	RDrive(rpwr);
}

